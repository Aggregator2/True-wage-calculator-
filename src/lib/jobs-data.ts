// Helper function to get all jobs flat
export function getAllJobs(): { job: Job; industry: Industry }[] {
  return INDUSTRIES.flatMap(industry =>
    industry.jobs.map(job => ({ job, industry }))
  );
}

// Helper function to get job by ID
export function getJobById(jobId: string): { job: Job; industry: Industry } | undefined {
  for (const industry of INDUSTRIES) {
    const job = industry.jobs.find(j => j.id === jobId);
    if (job) {
      return { job, industry };
    }
  }
  return undefined;
}

