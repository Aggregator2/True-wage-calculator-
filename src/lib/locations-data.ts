// Helper function to search locations
export function searchLocations(query: string, region?: LocationType): Location[] {
  const normalizedQuery = query.toLowerCase().trim();

  return ALL_LOCATIONS.filter(loc => {
    const matchesRegion = !region || loc.region === region;
    const matchesQuery = !normalizedQuery ||
      loc.name.toLowerCase().includes(normalizedQuery) ||
      loc.country.toLowerCase().includes(normalizedQuery) ||
      loc.popularFor.some(tag => tag.toLowerCase().includes(normalizedQuery));

    return matchesRegion && matchesQuery;
  });
}

// Get locations grouped by region
export function getLocationsByRegion(): Record<LocationType, Location[]> {
  return {
    uk: UK_LOCATIONS,
    europe: EUROPE_LOCATIONS,
    americas: AMERICAS_LOCATIONS,
    asia: ASIA_LOCATIONS,
    middle_east: MIDDLE_EAST_AFRICA_LOCATIONS.filter(l => l.region === 'middle_east'),
    oceania: OCEANIA_LOCATIONS,
    africa: MIDDLE_EAST_AFRICA_LOCATIONS.filter(l => l.region === 'africa'),
  };
}
